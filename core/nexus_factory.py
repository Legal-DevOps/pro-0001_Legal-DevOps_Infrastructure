def build_content(sections):
    def builder(story, styles):
        from reportlab.platypus import Paragraph, HRFlowable, Spacer
        from reportlab.lib.units import cm
        
        for sec_title, items in sections:
            if sec_title:
                story.append(Paragraph(f"▸ {sec_title}", styles['h2']))
                story.append(HRFlowable(width="100%", thickness=0.5, color=LayoutConfig.BORDER))
                story.append(Spacer(1, 0.2*cm))
            for item in items:
                if item.startswith("•"):
                    story.append(Paragraph(item, styles['bullet']))
                else:
                    story.append(Paragraph(item, styles['body']))
        return story
    return builder

